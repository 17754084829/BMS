package model;


public class User {
	private String username;
	private String passwd;
	
	public String getPasswd() {
		return passwd;
	}
	public String getUsername() {
		return username;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public void setUsername(String username) {
		this.username = username;
	}
}
